#Level 1: 합 구하기
#사용자로부터 양의 정수를 입력받고, 1부터 해당 숫자까지의 모든 정수의 합을 계산하는 프로그램을 작성하세요.

#Level 2: 팩토리얼 계산
#양의 정수를 입력받고, 해당 숫자의 팩토리얼을 계산하는 프로그램을 작성하세요.

#Level 3: 구구단 출력
#양의 정수를 입력받고, 해당 숫자의 구구단을 출력하는 프로그램을 작성하세요.

#Level 4: 소수 판별
#양의 정수를 입력받고, 해당 숫자가 소수인지 아닌지를 판별하는 프로그램을 작성하세요.

#Level 5: 문자열 뒤집기
#사용자로부터 문자열을 입력받고, 그 문자열을 뒤집어서 출력하는 프로그램을 작성하세요.

#Level 6: 리스트에서 최대값 찾기
#주어진 리스트에서 최대값을 찾아내는 프로그램을 작성하세요.

#Level 7: 단어 세기
#사용자로부터 문장을 입력받고, 각 단어가 몇 번 나오는지를 세어 출력하는 프로그램을 작성하세요.

#Level 8: 소수 리스트
#양의 정수 N을 입력받고, 2부터 N까지의 소수들을 리스트로 출력하는 프로그램을 작성하세요.

#Level 9: 리스트 정렬
#주어진 리스트를 오름차순이나 내림차순으로 정렬하는 프로그램을 작성하세요. (정렬 알고리즘 사용 금지)

#Level 10: 피보나치 수열
#양의 정수 N을 입력받고, N개의 피보나치 수열을 출력하는 프로그램을 작성하세요.